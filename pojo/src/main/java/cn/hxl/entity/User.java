package cn.hxl.entity;
/**
 * 用户类
 * @author hp
 *
 */
public class User {

	private int id;	//id
	private String name;	//姓名
	private int sex;	//性别

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
		
		
	
	
}
